-- init schema for Postgres (converted from MySQL dump)

CREATE TABLE IF NOT EXISTS locations (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name VARCHAR(50) NOT NULL
);

CREATE TABLE IF NOT EXISTS concerts (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  artist VARCHAR(50) NOT NULL,
  location_id BIGINT NOT NULL REFERENCES locations(id)
);

CREATE TABLE IF NOT EXISTS shows (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  concert_id BIGINT NOT NULL REFERENCES concerts(id),
  start TIMESTAMPTZ NOT NULL,
  "end" TIMESTAMPTZ NOT NULL
);

CREATE TABLE IF NOT EXISTS bookings (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name VARCHAR(100) NOT NULL,
  address VARCHAR(100) NOT NULL,
  city VARCHAR(100) NOT NULL,
  zip VARCHAR(10) NOT NULL,
  country VARCHAR(100) NOT NULL,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS reservations (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  token VARCHAR(255) NOT NULL,
  expires_at TIMESTAMPTZ NOT NULL
);

CREATE TABLE IF NOT EXISTS tickets (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  code VARCHAR(10) NOT NULL,
  booking_id BIGINT NOT NULL REFERENCES bookings(id),
  created_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS location_seat_rows (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name VARCHAR(50) NOT NULL,
  "order" INT NOT NULL,
  show_id BIGINT NOT NULL REFERENCES shows(id),
  CONSTRAINT location_seat_rows_name_show_id_unique UNIQUE (name, show_id),
  CONSTRAINT location_seat_rows_order_show_id_unique UNIQUE ("order", show_id)
);

CREATE TABLE IF NOT EXISTS location_seats (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  location_seat_row_id BIGINT NOT NULL REFERENCES location_seat_rows(id),
  number INT NOT NULL,
  reservation_id BIGINT NULL REFERENCES reservations(id),
  ticket_id BIGINT NULL REFERENCES tickets(id),
  CONSTRAINT location_seats_location_seat_row_id_number_unique UNIQUE (location_seat_row_id, number)
);

